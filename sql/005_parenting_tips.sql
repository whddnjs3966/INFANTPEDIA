-- 육아 꿀팁 테이블 생성 및 시드 데이터

CREATE TABLE IF NOT EXISTS parenting_tips (
  id BIGSERIAL PRIMARY KEY,
  month_id INTEGER NOT NULL REFERENCES months_info(month),
  category TEXT NOT NULL CHECK (category IN ('sleep_tips', 'feeding_tips', 'crying_tips', 'outing_tips')),
  title TEXT NOT NULL,
  content TEXT NOT NULL,
  created_at TIMESTAMPTZ DEFAULT NOW()
);

-- 인덱스
CREATE INDEX idx_parenting_tips_month_id ON parenting_tips(month_id);
CREATE INDEX idx_parenting_tips_category ON parenting_tips(category);

-- RLS
ALTER TABLE parenting_tips ENABLE ROW LEVEL SECURITY;
CREATE POLICY "Allow anonymous read" ON parenting_tips FOR SELECT USING (true);

